func ReadFile1(path string) []string {
	fileHandle, err := os.OpenFile(path, os.O_RDONLY, 0666)
	if err != nil {
		panic(err)
	}

	defer fileHandle.Close()

	readBytes, err := ioutil.ReadAll(fileHandle)
	if err != nil {
		panic(err)

	}

	results := strings.Split(string(readBytes), "\n")

	//fmt.Printf("read result:%v", results)
	return results
}

